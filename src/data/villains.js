export function getVillainById(id) {
  return VILLAINS.find(v => v.id === id)
}

export function getPowerById(id) {
  return POWERS.find(p => p.id === id)
}

export function getVillainProgress(villainId, completedChallenges) {
  const villain = getVillainById(villainId)
  if (!villain) return { done: 0, total: 0, defeated: false }
  const done = villain.challengeIds.filter(id => completedChallenges.includes(id)).length
  return { done, total: villain.challengeIds.length, defeated: done >= villain.challengeIds.length }
}
